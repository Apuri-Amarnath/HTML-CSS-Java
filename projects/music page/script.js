
function back() {
    Element1 = document.getElementById("page-1");
    Element2 = document.getElementById("page-2")
    Element1.style.display = "block";
    Element2.style.display = "none";
}
function change() {
    Element1 = document.getElementById("page-1");
    Element2 = document.getElementById("page-2")
    Element2.style.display = "block";
    Element1.style.display = "none";
}