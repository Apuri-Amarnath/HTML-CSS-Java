
function change() {
    var element = document.getElementById("container");
    element.style.display = "none";
    var element1 = document.getElementById('otherdiv');
    element1.style.display = "block";
}
function Back(){
    var element = document.getElementById("container");
    element.style.display = "block";
    var element1 = document.getElementById("otherdiv");
    element1.style.display = "none";
}